package cn.net.dbi.param.bean;

public class ParamValue {
	
	private String uuid;
	
	private String defineUuid;
	
	private String name;
	
	private int seq;

	public String getUuid() {
		return uuid;
	}

	public void setUuid(String uuid) {
		this.uuid = uuid;
	}

	public String getDefineUuid() {
		return defineUuid;
	}

	public void setDefineUuid(String defineUuid) {
		this.defineUuid = defineUuid;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public int getSeq() {
		return seq;
	}

	public void setSeq(int seq) {
		this.seq = seq;
	}
	
	
}
